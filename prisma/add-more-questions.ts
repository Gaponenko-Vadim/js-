// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –≤–æ–ø—Ä–æ—Å–æ–≤ –∫ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–º—É —Ç–µ—Å—Ç—É
async function addQuestionsToTest(testTitle: string, newQuestions: QuestionData[]) {
  const test = await prisma.test.findFirst({
    where: { title: testTitle },
    include: { questions: true }
  });

  if (!test) {
    console.log(`‚ùå Test "${testTitle}" not found`);
    return;
  }

  const startOrder = test.questions.length;

  for (let i = 0; i < newQuestions.length; i++) {
    const q = newQuestions[i];

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —É–∂–µ —Ç–∞–∫–æ–π –≤–æ–ø—Ä–æ—Å
    let question = await prisma.question.findFirst({
      where: {
        question: q.question,
        correctAnswer: q.correctAnswer,
      }
    });

    // –ï—Å–ª–∏ –≤–æ–ø—Ä–æ—Å–∞ –Ω–µ—Ç, —Å–æ–∑–¥–∞—ë–º –µ–≥–æ
    if (!question) {
      question = await prisma.question.create({
        data: {
          question: q.question,
          options: q.options,
          correctAnswer: q.correctAnswer,
          explanation: q.explanation,
        }
      });
    }

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ —Å–≤—è–∑–∞–Ω –ª–∏ —É–∂–µ –≤–æ–ø—Ä–æ—Å —Å —Ç–µ—Å—Ç–æ–º
    const existingLink = await prisma.testQuestion.findFirst({
      where: {
        testId: test.id,
        questionId: question.id,
      }
    });

    // –°–æ–∑–¥–∞—ë–º —Å–≤—è–∑—å —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ—ë –µ—â—ë –Ω–µ—Ç
    if (!existingLink) {
      await prisma.testQuestion.create({
        data: {
          testId: test.id,
          questionId: question.id,
          order: startOrder + i,
        }
      });
    }
  }

  console.log(`‚úÖ Added ${newQuestions.length} questions to "${testTitle}" (total: ${startOrder + newQuestions.length})`);
}

