async function main() {
  console.log('üå± –ù–∞—á–∏–Ω–∞–µ–º –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö —Ç–µ—Å—Ç–∞–º–∏...');

  // –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –≤–æ–ø—Ä–æ—Å—ã
  const uniqueQuestions = new Map();

  tests.forEach((test) => {
    test.questions.forEach((q: any) => {
      const key = `${q.question}-${q.correctAnswer}`;
      if (!uniqueQuestions.has(key)) {
        uniqueQuestions.set(key, {
          question: q.question,
          options: q.options,
          correctAnswer: q.correctAnswer,
          explanation: q.explanation,
        });
      }
    });
  });

  // –°–æ–∑–¥–∞—ë–º –≤—Å–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –≤–æ–ø—Ä–æ—Å—ã
  console.log(`üìù –°–æ–∑–¥–∞—ë–º ${uniqueQuestions.size} —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤...`);
  const createdQuestions: any[] = [];
  for (const questionData of uniqueQuestions.values()) {
    const question = await prisma.question.create({
      data: questionData,
    });
    createdQuestions.push(question);
  }
  console.log(`‚úÖ –°–æ–∑–¥–∞–Ω–æ ${createdQuestions.length} –≤–æ–ø—Ä–æ—Å–æ–≤`);

  // –°–æ–∑–¥–∞—ë–º —Ç–µ—Å—Ç—ã –∏ —Å–≤—è–∑—ã–≤–∞–µ–º —Å –≤–æ–ø—Ä–æ—Å–∞–º–∏ —á–µ—Ä–µ–∑ TestQuestion
  for (const test of tests) {
    const createdTest = await prisma.test.create({
      data: {
        title: test.title,
        description: test.description,
        difficulty: test.difficulty,
      },
    });

    // –°–≤—è–∑—ã–≤–∞–µ–º –≤–æ–ø—Ä–æ—Å—ã —Å —Ç–µ—Å—Ç–æ–º
    for (let i = 0; i < test.questions.length; i++) {
      const q = test.questions[i];
      const key = `${q.question}-${q.correctAnswer}`;
      const questionData = uniqueQuestions.get(key);

      // –ù–∞—Ö–æ–¥–∏–º —Å–æ–∑–¥–∞–Ω–Ω—ã–π –≤–æ–ø—Ä–æ—Å
      const question = createdQuestions.find(
        (cq) => cq.question === questionData.question && cq.correctAnswer === questionData.correctAnswer
      );

      if (question) {
        await prisma.testQuestion.create({
          data: {
            testId: createdTest.id,
            questionId: question.id,
            order: i,
          },
        });
      }
    }

    console.log(`‚úÖ –°–æ–∑–¥–∞–Ω —Ç–µ—Å—Ç: ${test.title} (${test.questions.length} –≤–æ–ø—Ä–æ—Å–æ–≤)`);
  }

  console.log('üéâ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö —É—Å–ø–µ—à–Ω–æ –∑–∞–ø–æ–ª–Ω–µ–Ω–∞!');
  console.log(`üìä –°–æ–∑–¥–∞–Ω–æ —Ç–µ—Å—Ç–æ–≤: ${tests.length}`);
  console.log(`   - Beginner: 5 —Ç–µ—Å—Ç–æ–≤`);
  console.log(`   - Intermediate: 7 —Ç–µ—Å—Ç–æ–≤`);
  console.log(`   - Advanced: 6 —Ç–µ—Å—Ç–æ–≤`);
  console.log(`üìù –í—Å–µ–≥–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤: ${createdQuestions.length}`);
}

